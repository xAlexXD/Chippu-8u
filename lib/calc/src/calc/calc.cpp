#include "calc.h"

int Calculator::add(const int &lhs, const int &rhs) {
    return lhs + rhs;
}

int Calculator::subtract(const int &lhs, const int &rhs) {
    return lhs - rhs;
}

int Calculator::multiply(const int &lhs, const int &rhs) {
    return lhs * rhs;
}

int Calculator::divide(const int &lhs, const int &rhs) {
    return lhs / rhs;
}
